#!/bin/sh

# Fix Postfix Instance Conflict Script
# This script fixes the postmulti instance conflict error

set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

print_status() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

print_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

print_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

print_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Check if running as root
if [ "$(id -u)" -ne 0 ]; then
    print_error "This script must be run as root (use sudo)"
    exit 1
fi

print_status "Fixing Postfix instance conflict..."

# Stop Postfix first
print_status "Stopping Postfix..."
systemctl stop postfix

# Check for postmulti instances
print_status "Checking for postmulti instances..."
postmulti -l 2>/dev/null || true

# Remove any conflicting instances
print_status "Removing conflicting postmulti instances..."
postmulti -d 2>/dev/null || true

# Clean up any leftover instance configurations
print_status "Cleaning up instance configurations..."
rm -f /etc/postfix/master.cf.* 2>/dev/null || true
rm -f /etc/postfix/main.cf.* 2>/dev/null || true

# Ensure we have a clean master.cf
print_status "Creating clean master.cf..."
cat > /etc/postfix/master.cf << 'EOF'
#
# Postfix master process configuration file.  For details on the format
# of the file, see the master(5) manual page (command: "man 5 master" or
# on-line: http://www.postfix.org/master.5.html).
